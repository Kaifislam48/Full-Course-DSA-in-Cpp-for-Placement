#include <iostream>
using namespace std;

int main(){
    int age = 5;
    char grade = 'A';
    bool isAdult = true;
    float cgpa = 9.2;
    cout<<age<<" "<<grade<<" "<<isAdult<<" "<<cgpa<<endl;
    cout<<"Size of Int: "<<sizeof(int)<<endl;
    cout<<"Size of Char: "<<sizeof(char)<<endl;
    cout<<"Size of Boolean: "<<sizeof(bool)<<endl;
    cout<<"Size of Float: "<<sizeof(float)<<endl;
}